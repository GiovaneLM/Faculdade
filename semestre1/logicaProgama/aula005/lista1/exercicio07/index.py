# Exercício 07.
# Mostrar "Olá" a quantidade de vezes informada pelo usuário

# Inicializa a variável de controle do início
inicio = 0

# Inicializa a variável que vai guardar o limite
fim = 0

# Lê quantas vezes o usuário quer repetir a mensagem
fim = int(input("Digite quanta vezes tu quer que Ola seja repitida: "))

# Executa o loop enquanto inicio for menor que fim
while inicio < fim:

    # Imprime a mensagem "Olá"
    print("Ola")

    # Incrementa o contador
    inicio += 1