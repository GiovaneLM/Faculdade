'''Exercício 01. Faça um algoritmo que imprima dez vezes o caracter "#" um abaixo do outro. Utilize o comando for.'''

# Cria uma lista com vários caracteres "#"
lista = ["#","#","#","#","#","#","#","#","#","#","#",]

# Percorre cada elemento da lista
for i in lista:

    # Imprime cada elemento em uma nova linha
    print(i)

# Imprime "fim" após o loop
print("fim")


# Repete o loop 10 vezes (de 0 até 9)
for i in range(10):

    # Imprime "#" em uma nova linha a cada repetição
    print("#")